def get_thac0(class_name: ClassName, level: int) -> tuple[int, int]:
    """Return (thac0, bthb) for given class and level."""
    table = THAC0_TABLES[class_name]
    for max_lvl, thac0, bthb in table:
        if level <= max_lvl:
            return thac0, bthb
    return table[-1][1], table[-1][2]


